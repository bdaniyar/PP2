#Upper case
a = "Hello, World!"
print(a.upper())

#Lower case
a = "Hello, World!"
print(a.lower())

#strip
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

#replace
a = "Hello, World!"
print(a.replace("H", "J"))

#split
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']

